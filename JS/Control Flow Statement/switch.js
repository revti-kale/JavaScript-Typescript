let month = 9;
let monthName;

switch (month) {
    case 1:
        monthName = 'jan';
        break;
    case 2:
        monthName = 'feb';
        break;
    case 3:
        monthName = 'mar';
        break;
    case 4:
        monthName = 'apr';
        break;
    case 5:
        monthName = 'may';
        break;
    case 6:
        monthName = 'jun';
        break;
    default:
        monthName = 'invalid'
}
console.log(monthName)